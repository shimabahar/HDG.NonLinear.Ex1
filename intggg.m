function out = intggg(V,e,np,a,b)
global n

if np == 3
  x = [-(3/5)^(1/2), 0, (3/5)^(1/2)];
  w = [5/9, 8/9, 5/9];
elseif np == 4
  x = [0.8611363116, 0.3399810436, -0.3399810436, -0.8611363116];
  w = [0.3478548451, 0.6521451549, 0.6521451549, 0.3478548451];
elseif np == 5
  x = [0.9061798459, 0.5384693101, 0, -0.5384693101, -0.9061798459];
  w = [0.2369268850, 0.4786286705, 0.5688888889, 0.4786286705, 0.2369268850];
end

o = 0;
oo = 0;
for i = 1:np
    for j = 1:n
        o = V(j) * phi(j, e, (a+b)/2+((b-a)/2)*x(i)) + o;
    end
    oo = w(i) * (o-Q0((a+b)/2+((b-a)/2)*x(i)))^2 + oo;
    o = 0;
end

out = ((b-a)/2) * oo;